package com.el.cmic.domain.record;

import java.util.List;

import com.alibaba.fastjson.annotation.JSONField;

/**
 * 养护记录推送   实体
 * @author zhanhao
 *
 */
public class Conserver {
	
	@JSONField(name="明细")
	private List<ConserveDetail> conserveDetail;

	public List<ConserveDetail> getConserveDetail() {
		return conserveDetail;
	}

	public void setConserveDetail(List<ConserveDetail> conserveDetail) {
		this.conserveDetail = conserveDetail;
	}

	@Override
	public String toString() {
		return "Conserver [conserveDetail=" + conserveDetail
				+ ", getConserveDetail()=" + getConserveDetail()
				+ ", getClass()=" + getClass() + ", hashCode()=" + hashCode()
				+ ", toString()=" + super.toString() + "]";
	}
}

package com.el.cmic.domain.account;

import java.util.List;

import com.alibaba.fastjson.annotation.JSONField;

public class Account {
	
	@JSONField(name="明细")
	public List<AccountItem> acountItem;

	public List<AccountItem> getAcountItem() {
		return acountItem;
	}

	public void setAcountItem(List<AccountItem> acountItem) {
		this.acountItem = acountItem;
	}

	@Override
	public String toString() {
		return "Account [acountItem=" + acountItem + "]";
	}
	
	

}
